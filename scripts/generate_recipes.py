def normalize_title_catalog():
    """오타/이상 항목 정리."""
    fixed = []
    for item in CATALOG:
        title = item[0]
        # 영문 혼입 오타 교정
        if title.startswith("connext"):
            title = "리코타치즈샐러드"
        fixed.append((title,) + item[1:])
    return fixed


def main():
    existing = []
    if os.path.exists(EXISTING):
        existing = json.load(open(EXISTING, encoding="utf-8"))
    existing_titles = {r["title"] for r in existing}
    print(f"기존 레시피: {len(existing)}개 (lessonCards 보존)")

    catalog = normalize_title_catalog()
    generated = []
    seen = set(existing_titles)
    for (title, emoji, mains, method, diff, time, serv, desc) in catalog:
        if title in seen:
            continue
        seen.add(title)
        generated.append(
            build_recipe(title, emoji, mains, method, diff, time, serv, desc)
        )

    combined = existing + generated
    # 200개로 제한 (혹시 초과 시)
    combined = combined[:TARGET]

    print(f"생성 레시피: {len(generated)}개")
    print(f"합계: {len(combined)}개")

    # 검증
    assert all(r.get("title") for r in combined), "title 누락"
    assert all(r.get("ingredients") for r in combined), "ingredients 누락"
    assert all(len(r.get("steps", [])) >= 3 for r in combined), "steps 부족"

    json.dump(combined, open(OUT, "w", encoding="utf-8"),
              ensure_ascii=False, indent=1)
    print(f"✅ 저장: {OUT} ({len(combined)}개)")


if __name__ == "__main__":
    main()
