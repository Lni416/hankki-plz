    if servings == 1:
        tags.append("1인분")
    if time <= 15:
        tags.append("간단")
    if extra_tags:
        tags.extend(extra_tags)
    # 중복 제거, 최대 4개
    tags = list(dict.fromkeys(tags))[:4]

    return {
        "title": title,
        "emoji": emoji,
        "description": description,
        "difficulty": difficulty,
        "cookingTimeMinutes": time,
        "servings": servings,
        "category": category,
        "tags": tags,
        "ingredients": ingredients,
        "steps": steps,
        "nutrition": nutrition,
    }


