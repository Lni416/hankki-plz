    # 단계 생성
    steps = []
    tmpl = STEP_TEMPLATES.get(method, STEP_TEMPLATES["볶음"])
    for i, (desc, tip) in enumerate(tmpl, start=1):
        desc = desc.replace("{main}", main_name).replace("{mains}", main_names)
        desc = _fix_josa(desc)
        step = {"order": i, "description": desc}
        if tip:
            step["tip"] = tip
        # 대표 단계에 소요시간 부여
        if i == len(tmpl):
            step["durationMinutes"] = max(2, time // len(tmpl))
        steps.append(step)

