def _has_batchim(word: str) -> bool:
    """단어의 마지막 글자에 받침이 있는지."""
    if not word:
        return False
    last = word[-1]
    if "가" <= last <= "힣":
        return (ord(last) - 0xAC00) % 28 != 0
    # 숫자/영문으로 끝나면 받침 없는 것으로 처리
    return False


def _fix_josa(text: str) -> str:
    """'재료을(를)' 같은 양자택일 조사를 받침에 맞게 하나로 고친다."""
    def repl(pair):
        with_b, without_b = pair

        def _r(m):
            word = m.group(1)
            return word + (with_b if _has_batchim(word) else without_b)
        return _r

    text = re.sub(r"([가-힣A-Za-z0-9]+)을\(를\)", repl(("을", "를")), text)
    text = re.sub(r"([가-힣A-Za-z0-9]+)와\(과\)", repl(("과", "와")), text)
    text = re.sub(r"([가-힣A-Za-z0-9]+)이\(가\)", repl(("이", "가")), text)
    text = re.sub(r"([가-힣A-Za-z0-9]+)은\(는\)", repl(("은", "는")), text)
    return text
