/// 재료 이름 기반으로 일반적으로 알려진 유통기한(일)을 반환한다.
///
/// 부분 문자열(`contains`) 매칭으로 한국어 재료명을 대응시키고,
/// 매칭되는 항목이 없으면 카테고리 기본값으로 폴백한다.
/// (냉장 보관 기준 — 삼성 비스포크 식품관리 컨셉)
int shelfLifeDaysFor(String name, IngredientCategory category) {
  final n = name.toLowerCase().replaceAll(' ', '');

  for (final entry in _keywordShelfLife) {
    if (n.contains(entry.$1)) return entry.$2;
  }
  return _categoryShelfLife[category] ?? 7;
}

