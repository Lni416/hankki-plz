/// 재료 이름을 기반으로 적절한 이모지를 반환한다.
///
/// 부분 문자열(`contains`) 매칭으로 한국어 재료명을 이모지에 대응시키고,
/// 매칭되는 항목이 없으면 카테고리 기본 이모지로 폴백한다.
String emojiForIngredient(String name, IngredientCategory category) {
  final n = name.toLowerCase().replaceAll(' ', '');

  // 가장 구체적인 키워드가 앞에 오도록 정렬 (예: '닭가슴살' → '닭' 보다 먼저)
  for (final entry in _keywordEmoji) {
    if (n.contains(entry.$1)) return entry.$2;
  }
  return _categoryEmoji[category] ?? '🥘';
}

