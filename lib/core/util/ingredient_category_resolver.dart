IngredientCategory categoryForIngredient(String name) {
  final normalized = name.toLowerCase().replaceAll(RegExp(r'\s+'), '');

  for (final entry in _categoryKeywords.entries) {
    if (entry.value.any(normalized.contains)) {
      return entry.key;
    }
  }

  return IngredientCategory.vegetable;
}

